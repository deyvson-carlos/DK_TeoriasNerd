    <h4 style="font-size: 1.0rem;"><strong>
      O Multiverso Marvel é protegido pela entidade conhecida como o Tribunal Vivo, que é um dos poucos seres com a mesma representação em todas as realidades. É o Tribunal Vivo que evita que um universo acumule mais poder do que qualquer outro e perturbe o equilíbrio cósmico mantido pelo Aquele Acima de Tudo.<br>

      <br>Com o tempo, a Marvel se viu obrigada a explicar melhor seu Multiverso. Para isso, acrescentou em seu cânone a agência temporal Time Variance Authority (TVA), que ficou famosa com a estreia de Loki e observa discrepâncias temporais para manter a “continuidade oficial”; e a Captain Britain Corps, grupo formado pelos Capitães Britânia e liderado pelo mago Merlin para manter a coesão da Terra-616, que é a Terra do principal universo.<br>

      <br>O Multiverso Marvel possui infinitas realidades, nas quais sempre há um personagem destacado como epicentro das histórias. Isso facilita identificar cada uma delas. Além disso, há alguns seres e grupos que existem em uma espécie de “limbo”, posicionado fora de linhas alternativas ou mundos paralelos. É o caso da TVA, do Tribunal Vivo e da Corporação dos Capitães Britânia<br>

      <br> Vale destacar que certas dimensões do Multiverso Marvel não fazem parte de um universo paralelo independente e estão em uma zona comum de acesso a várias realidades. Este é caso do Microverso, do Mojoverso, da Zona Negativa, do Outro Lugar, do Inferno e de outros mundos que não estão exatamente atrelados a uma Terra específica.
