  <h2><b>Além de Morbius: Conheça os próximos filmes do Universo Homem-Aranha da Sony</b></h2>
      <p>Saiba quais são os próximos personagens da Marvel a ganhar filmes do estúdio</p><br>
 

      <p>Dona dos direitos do Homem-Aranha nos cinemas desde 1999, a Sony sempre foi responsável por produzir filmes do herói da Marvel. Foi assim com a trilogia dos anos 2000, com Tobey Maguire, e também com a dobradinha O Espetacular Homem-Aranha, com Andrew Garfield.<br>

      <br>Tudo mudou em 2015, quando a empresa fez um acordo com o Marvel Studios e permitiu a inclusão do Amigão da Vizinhança no Universo Cinematográfico da Marvel. Dessa forma, Tom Holland ganhou uma trilogia própria e até apareceu nos filmes dos Vingadores.<br>

      <br>O acordo é uma parceria e a Sony segue dona dos direitos do Homem-Aranha. Porém, não ter mais controle exclusivo do Cabeça de Teia não impediu o estúdio de lançar um universo cinematográfico com filmes focados em personagens ligados ao herói. Foi o caso de Venom (2018), Venom: Tempo de Carnificina (2021) e o recente Morbius (2022).<br>

      <br>Chamada Sony’s Spider-Man Universe (Universo Homem-Aranha da Sony, em tradução livre), essa empreitada atualmente conta com vários filmes e séries em desenvolvimento. Para que você conheça esses projetos, reunimos abaixo uma lista com todas as informações disponíveis até o momento:
    </p>

    <!-- kraven -->
   <div class="bg-image">
        <h1 class="titulo">Kraven, O Caçador – Filme</h1>
          <img src="../img/sony/kraven-o-cacador-filme.jpg"
              class="img-fluid shadow-2-strong rounded-5 mb-4" alt="" />
    </div>
            <p>O próximo vilão do Homem-Aranha a ganhar um filme solo será Kraven, O Caçador. Com direção de J.C. Chandor (Até o Fim) e roteiro de Richard Wenk (Os Mercenários 2), Art Marcum e Matt Holloway (Homem de Ferro), a produção terá Aaron Taylor-Johnson (Kick-Ass; Vingadores: Era de Ultron) como protagonista.<br>

            <br>Não foram revelados detalhes da trama, mas ela deve contar como Sergei Kravinoff (Johnson) se tornou um dos maiores caçadores do mundo. Completam o elenco Ariana DeBose como Calypso, Fred Hechinger como Camaleão e Russell Crowe em papel secreto.<br>

            <br>O filme solo de Kraven, O Caçador tem estreia marcada para 13 de janeiro de 2023.</p><br>
<!-- ============================================= -->

        <!-- El Muerto -->
    <div class="bg-image">
        <h1 class="titulo">El Muerto – Filme</h1>
          <img src="../img/sony/el_muerto_bad_bunny__a11293i-768x350.jpg"
              class="img-fluid shadow-2-strong rounded-5 mb-4" alt="" />
    </div>
            <p>A Sony pegou os fãs do Homem-Aranha de surpresa ao anunciar um filme solo de El Muerto, vilão que só apareceu em duas HQs do Cabeça de Teia. Nos quadrinhos, Juan-Carlos Sánchez faz parte de uma longa linhagem de combatentes de luta livre que ganham poderes especiais de uma máscara mística. Ao recusar passar pelo ritual em que ganharia o direito de vestir o adereço, ele acaba punido e após uma década de treinamentos precisa derrotar um herói mascarado para provar merecer a dádiva. E é claro que o escolhido é o Amigão da Vizinhança.<br>

            <br>O El Muerto dos cinemas será interpretado pelo cantor e ator porto-riquenho Bad Bunny. Sem detalhes sobre diretor ou roteirista, o filme chegará aos cinemas em 11 de janeiro de 2024.</p><br>
<!-- ======================================================== -->

      <!-- Venom 3 -->
    <div class="bg-image">
        <h1 class="titulo">Venom 3 – Filme</h1>
          <img src="../img/sony/venom-3-filme-760x345.jpg"
              class="img-fluid shadow-2-strong rounded-5 mb-4" alt="" />
    </div>
            <p>Em 2018, o primeiro filme solo do Venom foi um inesperado sucesso de bilheteria. Na época, o ator Tom Hardy revelou que assinou contrato para estrelar três filmes do anti-herói simbiônico.<br>

            <br>Pouco após o lançamento de Venom: Tempo de Carnificina, a produtora Amy Pascal confirmou que Venom 3 já está em produção. Com o desenvolvimento em estágios iniciais, ainda não se sabe qual será a trama ou quais personagens estão envolvidos.<br>

            <br> O terceiro filme do Venom não tem data de estreia definida.</p><br>
<!-- ======================================================== -->

    <!-- Madame Teia -->
      <div class="bg-image">
              <h1 class="titulo">Madame Teia – Filme</h1>
                <img src="../img/sony/madame-teia-filme-760x345.jpg"
                    class="img-fluid shadow-2-strong rounded-5 mb-4" alt="" />
          </div>
                  <p>Nos quadrinhos, a Madame Teia é Cassandra Webb, uma coadjuvante nas histórias do Homem-Aranha. Cega e com deficiência motora, a personagem é uma médium poderosíssima graças a dons como telepatia e clarividência.<br>

                  <br>A personagem será interpretada por Dakota Johnson (A Filha Perdida) em um filme com direção de S.J. Clarkson (Jessica Jones) e roteiro de Burk Sharpless e Matt Sazama (Morbius). O projeto está nos estágios iniciais e até agora conta apenas com Johnson e Sydney Sweeney (Euphoria) no elenco.<br>

                  <br>Madame Web não tem data de estreia definida.</p><br>
<!-- ======================================================== -->

          <!-- Teia de Seda (Silk) -->
          <div class="bg-image">
                        <h1 class="titulo">Teia de Seda (Silk) – Série</h1>
                          <img src="../img/sony/teia-de-seda-silk-serie.jpg"
                              class="img-fluid shadow-2-strong rounded-5 mb-4" alt="" />
                    </div>
                            <p>Quando o universo da Sony começou a se expandir, surgiram rumores de que a Teia de Seda (Silk, no original) ganharia um filme próprio. Anos depois, o estúdio anunciou que a personagem ganhará uma série de TV live-action.<br>

                            <br>Em 2020, a Variety revelou que a série de Teia de Seda terá produção executiva de Phil Lord e Chris Miller (Homem-Aranha no Aranhaverso) e Amy Pascal, com Lauren Moon (Atypical) responsável pelo roteiro. O veículo ainda afirmou que a Amazon estava negociando para lançar a produção no Prime Video. Um ano depois, o Deadline divulgou que Tom Spezialy (Watchmen) foi escolhido como showrunner do projeto. Desde então não foram reveladas mais notícias.</p><br>
 <!-- ======================================================== -->
          
        <!-- Loteria  -->
          <div class="bg-image">
                        <h1 class="titulo">Loteria – Filme</h1>
                          <img src="../img/sony/loteria-jackpot-filme-760x345.jpg"
                              class="img-fluid shadow-2-strong rounded-5 mb-4" alt="" />
                    </div>
                            <p>No mesmo ano em que Venom chegou aos cinemas, surgiu um rumor de que a Sony estaria desenvolvendo alguns filmes baseados em personagens do Homem-Aranha. Um deles seria o da Loteria (Jackpot, no original). Em 2020, foi revelado que a produção terá roteiro de Marc Guggenheim, escritor conhecido por comandar o Arrowverso na TV e por escrever HQs de vários personagens de DC e Marvel – como a própria Loteria.<br>

                            <br>Nos quadrinhos, a primeira Loteria foi Sara Ehret, uma mulher que ganha superforça ao ser exposta a um vírus que reescreve seu DNA. Após algum tempo lutando contra o crime em Nova York, ela se aposenta e vende o traje e o nome a Alana Jobson. Sem poderes especiais, ela usa substâncias para ter seus dons.<br>

                            <br>Faz dois anos que o projeto não ganha novidades, então não é possível saber em qual estágio ele está. Por enquanto, o filme segue sem diretor ou elenco definidos.</p><br>
 <!-- ======================================================== -->

        <!-- Filmes de Olivia Wilde e Roberto Orci  -->
          <div class="bg-image">
                        <h1 class="titulo">Filmes de Olivia Wilde e Roberto Orci</h1>
                          <img src="../img/sony/homem-aranha-olivia-wilde.jpg"
                              class="img-fluid shadow-2-strong rounded-5 mb-4" alt="" />
                    </div>
                            <p>Em 2020, surgiram notícias de dois projetos misteriosos da Sony com personagens da Marvel. Primeiro o The Wrap divulgou que Roberto Orci (O Espetacular Homem-Aranha 2) foi contratado para escrever um novo filme.<br>

                            <br>Meses depois, o Deadline revelou que Olivia Wilde (Fora de Série) fechou um acordo com o estúdio para desenvolver e dirigir um projeto focado em heroínas ligadas ao Homem-Aranha. Com roteiro de Katie Silberman (Fora de Série), especulou-se que o projeto será focado na Mulher-Aranha.<br>

                            <br>Até o momento, nenhum dos filmes recebeu atualizações.</p><br>
 <!-- ======================================================== -->

        <!-- Silver Sable e Gata Negra -->
          <div class="bg-image">
                        <h1 class="titulo">Silver Sable e Gata Negra</h1>
                          <img src="../img/sony/silver-sable-gata-negra-760x345.jpg"
                              class="img-fluid shadow-2-strong rounded-5 mb-4" alt="" />
                    </div>
                            <p>Um dos derivados do Homem-Aranha mais antigos, o encontro entre Gata Negra e Silver Sable já passou por todo tipo de mudança. A ideia inicial era o lançamento de Silver & Black, um filme protagonizado pelas duas. Ele foi cancelado e desmembrado em dois longas diferentes, cada um protagonizado por uma das personagens.<br>

                            <br>A última atualização veio em em 2020, quando a diretora Gina Prince-Bythewood disse que ainda tinha esperanças de que o projeto seja levado adiante. Segundo ela, a Sony está considerando transformar Silver & Black em uma minissérie.</p><br>
 <!-- ======================================================== -->

                    <!-- Sexteto Sinistro -->
                    <div class="bg-image">
                        <h1 class="titulo">Sexteto Sinistro (Filme)</h1>
                          <img src="../img/sony/sexteto-sinistro-filme-760x345.jpg"
                              class="img-fluid shadow-2-strong rounded-5 mb-4" alt="" />
                    </div>
                            <p>Não é segredo para ninguém que a Sony está ansiosa para levar o Sexteto Sinistro, a equipe de super-vilões do Homem-Aranha, para os cinemas. Na época dos filmes de O Espetacular Homem-Aranha, o estúdio chegou a contratar Drew Goddard (O Segredo da Cabana) para escrever e dirigir a produção. Porém, o acordo entre a empresa e o Marvel Studios fez com que o projeto fosse cancelado.<br>

                            <br>O Sexteto Sinistro voltou para a mira da Sony após o sucesso de Venom nas bilheterias. Em entrevista à Variety sobre o futuro do universo aracnídeo do estúdio, a produtora Amy Pascal deixou as portas abertas para que Goddard volte ao projeto. Segundo ela, o estúdio só está “esperando” pelo cineasta.</p><br>
 <!-- ======================================================== -->

