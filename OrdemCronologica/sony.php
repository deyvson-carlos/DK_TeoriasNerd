                    </div>
                            <p>Muitos dos projetos da lista chegaram aos ouvidos do público em 2018, quando a Variety revelou que a Sony buscava roteiristas para escrever filmes de Loteria, Teia de Seda e Nightwatch. Enquanto os dois primeiros projetos seguiram em frente, o terceiro segue abandonado desde então. Na época, ventilou-se que Spike Lee (Infiltrado na Klan) pudesse comandar a produção, mas o próprio cineasta negou os rumores.</p><br>
